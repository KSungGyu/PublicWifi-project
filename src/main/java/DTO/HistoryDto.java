package DTO;

public class HistoryDto {
    private int historyId;
    private double historyX;
    private double historyY;
    private String historyDate;

    public int getHistoryId() {
        return historyId;
    }

    public void setHistoryId(int historyId) {
        this.historyId = historyId;
    }

    public double getHistoryX() {
        return historyX;
    }

    public void setHistoryX(double historyX) {
        this.historyX = historyX;
    }

    public double getHistoryY() {
        return historyY;
    }

    public void setHistoryY(double historyY) {
        this.historyY = historyY;
    }

    public String getHistoryDate() {
        return historyDate;
    }

    public void setHistoryDate(String historyDate) {
        this.historyDate = historyDate;
    }
}
